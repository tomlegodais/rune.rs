fn encode_tab(items: Tab) -> Vec<Entry> {
    items.iter().map(|&(obj_id, amount)| Entry { obj_id, amount }).collect()
}

fn main() -> Result<()> {
    let args: Vec<String> = std::env::args().collect();
    if args.len() != 2 {
        bail!("Usage: seed-bank <username>\n\nExample:\n  seed-bank admin | psql -d rune -h localhost");
    }
    let username = args[1].replace('\'', "''");

    let tabs: Vec<Vec<Entry>> = vec![
        encode_tab(TAB_0_DEFAULT),
        encode_tab(TAB_1_FOOD),
        encode_tab(TAB_2_MELEE),
        encode_tab(TAB_3_RANGED),
        encode_tab(TAB_4_MAGIC),
        encode_tab(TAB_5_SKILLING),
        encode_tab(TAB_6_RUNES),
        encode_tab(TAB_7_TELEPORTS),
        encode_tab(TAB_8_RARES),
    ];

    let total: usize = tabs.iter().map(Vec::len).sum();
    eprintln!("Total bank entries: {total} / 516");

    let json = serde_json::to_string(&tabs)?;

    println!(
        "UPDATE player_bank SET objs = '{json}'::jsonb, last_x = 0 \
         WHERE player_id = (SELECT p.id FROM players p \
         JOIN accounts a ON p.account_id = a.id \
         WHERE a.username = '{username}');"
    );

    Ok(())
}
